#include <DOM/Node/CommentNode.hpp>

DOM::CommentNode::CommentNode(std::string content)
	: Node("COMMENT_NODE")
	, m_content(std::move(content))
{}

auto
DOM::CommentNode::clone() -> DOM::BoxNode
{
	return BoxNode(new CommentNode(m_content));
}

auto
DOM::CommentNode::comment() const -> std::string
{
	return m_content;
}
