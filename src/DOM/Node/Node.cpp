#include <DOM/Node/Node.hpp>

DOM::Node::Node(std::string tag_name) 
    : m_tag_name(std::move(tag_name))
{}

DOM::Node::~Node() = default;

auto 
DOM::Node::is(const std::string& tag) const -> bool
{
    return tag_name() == tag;
}

auto DOM::Node::tag_name() const -> std::string
{
  return m_tag_name;
}
